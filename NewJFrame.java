    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        
        //Set filter for Txt Files only!
         FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
         jFileChooser1.setFileFilter(filter);
         
         int returnVal = jFileChooser1.showOpenDialog(this);
         
         if (returnVal == jFileChooser1.APPROVE_OPTION) {
             
            File f = jFileChooser1.getSelectedFile();
            directory = f.getPath();
            path = f.getParent();
            jTextField1.setText(directory);
         }
    }                                        

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        
        upsplit split = new upsplit();
        
        try {
            // TODO add your handling code here:
            split.readFile(directory);
            split.createFiles(path);
            
        } catch (FileNotFoundException ex) {
            
            JOptionPane.showMessageDialog(null, "File not found!");
        }
        
        JOptionPane.showMessageDialog(null, "Split Complete! \nFiles have been stored in " + path);
    }                                        

